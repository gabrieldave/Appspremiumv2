export function Footer() {
  const [socialLinks, setSocialLinks] = useState<SocialMediaLink[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    loadSocialLinks();
  }, []);

  const loadSocialLinks = async () => {
    try {
      const { data, error } = await supabase
        .from('social_media_links')
        .select('*')
        .eq('is_active', true)
        .order('sort_order', { ascending: true });

      if (error) {
        console.error('Error loading social links:', error);
        setSocialLinks([]);
        return;
      }
      
      setSocialLinks(data || []);
    } catch (error) {
      console.error('Error loading social links:', error);
      setSocialLinks([]);
    } finally {
      setLoading(false);
    }
  };

  const getSocialIcon = (platform: string) => {
    return SocialIcons[platform] || (
      <svg className="w-6 h-6" fill="currentColor" viewBox="0 0 24 24">
        <path d="M18 16.08c-.76 0-1.44.3-1.96.77L8.91 12.7c.05-.23.09-.46.09-.7s-.04-.47-.09-.7l7.05-4.11c.54.5 1.25.81 2.04.81 1.66 0 3-1.34 3-3s-1.34-3-3-3-3 1.34-3 3c0 .24.04.47.09.7L8.04 9.81C7.5 9.31 6.79 9 6 9c-1.66 0-3 1.34-3 3s1.34 3 3 3c.79 0 1.5-.31 2.04-.81l7.12 4.16c-.05.21-.08.43-.08.65 0 1.61 1.31 2.92 2.92 2.92 1.61 0 2.92-1.31 2.92-2.92s-1.31-2.92-2.92-2.92z"/>
      </svg>
    );
  };

  return (
    <footer className="bg-slate-900 text-white py-12">
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="text-center">
          <h3 className="text-2xl font-bold mb-2">Todos Somos Traders</h3>
          <p className="text-slate-400 mb-6">
            Portal Premium de Herramientas de Trading Profesional
          </p>
          
          {/* Redes Sociales */}
          {!loading && socialLinks.length > 0 && (
            <div className="mb-8">
              <div className="flex flex-wrap justify-center items-center gap-4">
                {socialLinks.map((link) => (
                  <a
                    key={link.id}
                    href={link.url}
                    target="_blank"
                    rel="noopener noreferrer"
                    className="text-slate-300 hover:text-white transition-colors duration-200 hover:scale-110 transform"
                    aria-label={link.platform}
                    title={link.platform}
                  >
                    {getSocialIcon(link.platform)}
                  </a>
                ))}
              </div>
            </div>
          )}

          <p className="text-slate-500 text-sm">
            © {new Date().getFullYear()} Todos Somos Traders. Todos los derechos reservados.
          </p>
        </div>
      </div>
    </footer>
  );
}

